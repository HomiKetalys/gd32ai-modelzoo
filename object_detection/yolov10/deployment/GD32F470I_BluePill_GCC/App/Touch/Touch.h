#ifndef _TOUCH_H_
#define _TOUCH_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
#define POINT_NUM_MAX 5 //触摸点最大数量

/*********************************************************************************************************
*                                              枚举结构体
*********************************************************************************************************/
//坐标点
typedef struct
{
  u16 x;    //横坐标，0xFFFF表示无效值
  u16 y;    //纵坐标，0xFFFF表示无效值
  u16 size; //触点大小
}StructTouchPoint;

//触摸屏设备结构体
typedef struct
{
  u8               pointNum;                 //触摸点数，最多支持POINT_NUM_MAX个触摸点
  u8               pointFlag[POINT_NUM_MAX]; //触摸点按下标志位，1-触摸点按下，0-未检测到触摸点按下
  StructTouchPoint point[POINT_NUM_MAX];     //坐标点数据
}StructTouchDev;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void InitTouch(void);              //初始化触摸屏检测驱动模块
u8   ScanTouch(void);              //触屏扫描
StructTouchDev* GetTouchDev(void); //获取触摸设备结构体

#endif
