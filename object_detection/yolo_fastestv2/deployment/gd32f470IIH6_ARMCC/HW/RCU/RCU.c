static  void  ConfigRCU(void);  //配置RCC

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/

/*********************************************************************************************************
* 函数名称：ConfigRCC
* 函数功能：配置RCC 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：配置的时钟如下：
*           （1）PLLCLK（PLL时钟）    = 72MHz
*           （2）SYSCLK（系统时钟）   = 72MHz
*           （3）HCLK（AHB总线时钟）  = 72MHz
*           （4）PCLK1（APB1总线时钟）= 36MHz
*           （5）PCLK2（APB2总线时钟） = 72MHz
*           （6）ADCCLK（ADC时钟）    = 14MHz 
*********************************************************************************************************/
static void ConfigRCU(void)
{
  //定时器时钟全为200MHz
  rcu_timer_clock_prescaler_config(RCU_TIMER_PSC_MUL4);
}

/*********************************************************************************************************
*                                              API函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称：InitRCC
* 函数功能：初始化RCC模块 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2018年01月01日
* 注    意：
*********************************************************************************************************/
void InitRCU(void)
{
  ConfigRCU();  //配置RCC
}
