#ifndef _TEXT_WIDGET_H_
#define _TEXT_WIDGET_H_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
#define TEXT_WIDGET_BUF_SIZE 1024 * 5 / 2  //背景缓冲区大小，每个像素点2个字节，缓冲区大小为5k

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/
//text控件，该结构体需是静态变量
typedef struct
{
  u16 x0;     //text显示原点横坐标
  u16 y0;     //text显示原点纵坐标
  u16 width;  //text宽度，像素点
  u16 height; //text高度，像素点
  u16 size;   //text字体大小
  u16 color;  //字体颜色
  u16 background[TEXT_WIDGET_BUF_SIZE]; //背景缓冲区
  void (*setText)(void* this, const char* string);  //设置text内容
}StructTextWidget;

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
u8 CreateTextWidget(StructTextWidget* text);  //创建Text控件

#endif
